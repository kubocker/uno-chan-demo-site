      <h2>はじめに</h2>
      <p>kubocchi.com（以下、「当サイト」といいます。）は、ユーザーのプライバシーを尊重し、個人情報の保護に努めています。本プライバシーポリシー（以下、「本ポリシー」といいます。）は、当サイトが収集する個人情報の種類、利用目的、管理方法について説明します。</p>
    </section>
    <section>
      <h2>第1条（個人情報の収集方法）</h2>
      <p>当サイトは、ユーザーが利用登録をする際に氏名、メールアドレスなどの個人情報をお尋ねすることがあります。また、ユーザーと当サイトとの間で行われた取引記録や決済に関する情報を収集することがあります。</p>
    </section>
    <section>
      <h2>第2条（個人情報の利用目的）</h2>
      <p>当サイトが個人情報を収集・利用する目的は以下の通りです。</p>
      <ul>
        <li>サービスの提供・運営のため</li>
        <li>ユーザーからのお問い合わせに回答するため（本人確認を行うことを含む）</li>
        <li>ユーザーが利用中のサービスの新機能、更新情報、キャンペーン等及び当サイトが提供する他のサービスの案内のメールを送付するため</li>
        <li>メンテナンス、重要なお知らせなど必要に応じたご連絡のため</li>
        <li>利用規約に違反したユーザーや、不正・不当な目的でサービスを利用しようとするユーザーの特定をし、ご利用をお断りするため</li>
        <li>ユーザーにご自身の登録情報の閲覧や変更、削除、ご利用状況の閲覧を行っていただくため</li>
        <li>上記の利用目的に付随する目的</li>
      </ul>
    </section>
    <section>
      <h2>第3条（個人情報の第三者提供）</h2>
      <p>当サイトは、次に掲げる場合を除いて、あらかじめユーザーの同意を得ることなく、第三者に個人情報を提供することはありません。</p>
      <ul>
        <li>法令に基づく場合</li>
        <li>人の生命、身体または財産の保護のために必要がある場合であって、本人の同意を得ることが困難であるとき</li>
        <li>公衆衛生の向上または児童の健全な育成の推進のために特に必要がある場合であって、本人の同意を得ることが困難であるとき</li>
        <li>国の機関もしくは地方公共団体またはその委託を受けた者が法令の定める事務を遂行することに対して協力する必要がある場合であって、本人の同意を得ることにより当該事務の遂行に支障を及ぼすおそれがあるとき</li>
        <li>予め次の事項を告知あるいは公表し、かつ当サイトが個人情報保護委員会に届出をしたとき</li>
      </ul>
    </section>
    <section>
      <h2>第4条（個人情報の開示）</h2>
      <p>当サイトは、本人から個人情報の開示を求められたときは、本人に対し、遅滞なくこれを開示します。ただし、開示することにより次のいずれかに該当する場合は、その全部または一部を開示しないこともあり、その場合にはその旨を遅滞なく通知します。</p>
      <ul>
        <li>本人または第三者の生命、身体、財産その他の権利利益を害するおそれがある場合</li>
        <li>当サイトの業務の適正な実施に著しい支障を及ぼすおそれがある場合</li>
        <li>その他法令に違反することとなる場合</li>
      </ul>
      <p>個人情報の開示に際しては、1件あたり1,000円の手数料を申し受けます。</p>
    </section>
    <section>
      <h2>第5条（個人情報の訂正および削除）</h2>
      <p>ユーザーは、当サイトの保有する自己の個人情報が誤った情報である場合には、当サイトが定める手続きにより、個人情報の訂正、追加または削除（以下、「訂正等」といいます。）を請求することができます。当サイトは、ユーザーから訂正等の請求を受けた場合には、その請求に応じる必要があると判断した場合には、遅滞なく、これを行います。</p>
    </section>
    <section>
      <h2>第6条（個人情報の利用停止等）</h2>
      <p>当サイトは、本人から個人情報が利用目的の範囲を超えて取り扱われているという理由、または不正の手段により取得されたものであるという理由により、その利用の停止または消去（以下、「利用停止等」といいます。）を求められた場合には、遅滞なく必要な調査を行い、その結果に基づき、個人情報の利用停止等を行います。その結果を本人に通知します。ただし、個人情報の利用停止等に多額の費用を有する場合やその他利用停止等を行うことが困難な場合であって、本人の権利利益を保護するために必要な代わりの措置を講じることができる場合は、この代替案を講じます。</p>
    </section>
    <section>
      <h2>第7条（プライバシーポリシーの変更）</h2>
      <p>当サイトは、法令の変更やサービスの変更に伴い、事前の予告なく本ポリシーを変更することがあります。変更後のポリシーは、本サイトに掲載された時点で効力を生じます。</p>
